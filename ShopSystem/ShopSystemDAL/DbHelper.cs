        /// <summary>
        /// 获得批量删除名称显示值
        /// </summary>
        /// <param name="ColumnName">显示字段名称</param>
        /// <param name="TableName">表名称</param>
        /// <param name="IDList">ID集合字符串（带单引号的ID集合字符串）</param>
        /// <param name="IsLogic">是否是逻辑删除（默认：是）</param>
        /// <returns></returns>
        public string GetBatchText(string ColumnName, string TableName, string IDList, bool IsLogic = true)
        {
            string BatchText = "";
            StringBuilder strSql = new StringBuilder();
            strSql.Append("select " + ColumnName + " from " + TableName + "  where ID in (" + IDList + ")  ");
            if (IsLogic)
            {
                strSql.Append(" and DeleteState=0 ");
            }
            DataSet ds = Query(strSql.ToString());
            if (ds != null && ds.Tables.Count > 0 && ds.Tables[0] != null && ds.Tables[0].Rows.Count > 0)
            {
                for (int i = 0; i < ds.Tables[0].Rows.Count; i++)
                {
                    if (BatchText != "")
                    {
                        BatchText += "、";
                    }
                    BatchText += ds.Tables[0].Rows[i][ColumnName];
                }
            }
            return BatchText;
        }

